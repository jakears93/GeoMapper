package dev.archtech.geomapper.model.map;


public class Marker {
    private MarkerSize size;
    private MarkerColour colour;

    public MarkerSize getSize() {
        return size;
    }

    public void setSize(MarkerSize size) {
        this.size = size;
    }

    public MarkerColour getColour() {
        return colour;
    }

    public void setColour(MarkerColour colour) {
        this.colour = colour;
    }
}
